# 3-8 Seeing The World
dream_vacation = ['japan' , 'mexico' , 'puerto rico' , 'china' , 'singapore']

# print in orginal order
print(dream_vacation)

# use sorted() method
print(sorted(dream_vacation))

# show that the list is still in its orginal order
print(dream_vacation)

# use reverse to changethe order of your list
dream_vacation.reverse()
print(dream_vacation)

# change the list back to it's original order
dream_vacation.reverse()
print(dream_vacation)

# use sort()
dream_vacation.sort()
print(dream_vacation)

# use sort to put the list in reverse-alphabetical order
dream_vacation.sort(reverse=True)
print(dream_vacation)

# 3-9 use len to display the total number of desired destinations
print(len(dream_vacation))

