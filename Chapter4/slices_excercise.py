# 1. Print the first three items in the list using a slice.
# 2. Print the middle three items in the list using a slice.
# 3. print the last three items in the list using a slice.

my_foods = ['pizza', 'falafel', 'carrot cake' , 'cannoli', 'ice cream'] 

print("\nThe first three items in the list are:")
print(my_foods[:3])

print("\nThe middle three items in the list are:")
print(my_foods[1:4])

print("\nThe last three items in the list are:")
print(my_foods[-3:])


